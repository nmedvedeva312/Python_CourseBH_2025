"""
Запросить трижды ввод наименования товаров и их цену через пробел. 
"пример: 
>>>яблоко 10"
>>>груша 15
>>>малина 20
    
    - создать из введенных данных словарь где ключ это наименование, а цена значение
    - запросить имя товара, найти его в словаре, и вывести его цену, увеличенную на 15%. 
    - вывести сумму всех товаров

"""

# Вариант 1 без циклов

# Вводим данные в список из трех строк
entries = [
    input("Введите товар и цену через пробел: "),
    input("Введите товар и цену через пробел: "),
    input("Введите товар и цену через пробел: "),
]

# Преобразуем список строк в словарь: ключ - товар, значение - цена (float)
products = dict(
    (item.split()[0], float(item.split()[1]))
    for item in entries
)

item = input("Введите название товара для поиска: ")

# Получаем цену через get, если нет — None
price = products.get(item)

# Вывод с использованием or и тернарного оператора (без if/else в обычном виде)
print(
    price and f"Цена товара '{item}' с увеличением на 15%: {price * 1.15:.2f}"
    or "Товар не найден"
)

# Суммируем цены
total = sum(products.values())
print(f"Сумма всех товаров: {total:.2f}")


# Вариант 2 - через цикл

products = {}

for _ in range(3):
    name, price = input("Введите товар и цену через пробел: ").split()
    products[name] = float(price)

item = input("Введите название товара для поиска: ")

if item in products:
    increased_price = products[item] * 1.15
    print(f"Цена товара '{item}' с увеличением на 15%: {increased_price:.2f}")
else:
    print("Товар не найден")

total = sum(products.values())
print(f"Сумма всех товаров: {total:.2f}")
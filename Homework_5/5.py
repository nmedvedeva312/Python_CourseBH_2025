"""
Запросить фразу 
    - вывести на экран количество уникальных символов
    - вывести на экран количество уникальных слов
    -* вывести символ который встречался чаще всего

"""

phrase = input("Введите фразу: ")

# Количество уникальных символов (учитывая пробелы и знаки)
unique_chars = len(set(phrase))

# Разбиваем фразу на слова по пробелам
words = phrase.split()

# Количество уникальных слов
unique_words = len(set(words))

# Частота каждого символа (без использования циклов for)
from collections import Counter
counter = Counter(phrase)

# Символ, который встречается чаще всего
most_common_char = counter.most_common(1)[0][0]

print("Количество уникальных символов:", unique_chars)
print("Количество уникальных слов:", unique_words)
print("Самый часто встречающийся символ:", most_common_char)
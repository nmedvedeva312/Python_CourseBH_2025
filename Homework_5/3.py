"""
дан словарь
d = {'one':11, 'two':22, 'hello':'python', True:False}
запросить номер элемента и удалить его из словаря с помощью del.

"""

d = {'one':11, 'two':22, 'hello':'python', True:False}

index = int(input("Введите номер элемента (начиная с 0): "))

# Получаем список ключей словаря, чтобы обратиться к нужному ключу по индексу
key = list(d.keys())[index]

# Удаляем элемент из словаря по ключу
del d[key]

print("Обновлённый словарь:", d)

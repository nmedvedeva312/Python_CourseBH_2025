'''
Дан произвольный список из 5 элементов. 
     - Поменять местами 1ый и последний элемент
     - удалить и вывести на печать третий элемент
'''

items = [10, 20, 30, 40, 50]  # пример списка из 5 элементов

# Поменять местами первый и последний элемент
items[0], items[-1] = items[-1], items[0]

# Удалить и вывести третий элемент (индекс 2)
third_element = items.pop(2)
print("Удалённый третий элемент:", third_element)

# Вывести изменённый список
print("Изменённый список:", items)
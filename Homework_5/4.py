'''
Буква "a" стоит 10 очков, "b" - 20, "c" - 30, "d" - 40
Запросить кодовою фразу из пяти символов используя только a, b, c, d.
Вывести на экран общее количество очков введенной фразы.

'''

# Словарь с очками для каждой буквы
points = {'a': 10, 'b': 20, 'c': 30, 'd': 40}

# Запрашиваем у пользователя кодовую фразу из 5 символов
phrase = input("Введите кодовую фразу из 5 символов (только a, b, c, d): ")

# Считаем сумму очков для каждого символа в фразе
total_score = sum(points[char] for char in phrase)

# Выводим результат
print("Общее количество очков:", total_score)
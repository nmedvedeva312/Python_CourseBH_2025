'''
Запросить 3 раза строку из нескольких чисел через пробел
    - вывести все уникальные числа по возрастанию
    - вывести числа которые есть в каждой строке
    -* вывести числа которые есть только в одной из трех строк
    
    выполнить без циклов и условий
    
    пример:
    >>> 1 2 11 22
    >>> 1 2 22 33
    >>> 1 2 33 44


    1) 1 2 11 22 33 44
    2) 1 2
    3) 11 44
    
'''

# Запрос 3 строк чисел
s1 = input()
s2 = input()
s3 = input()

# Преобразуем строки в множества чисел
set1 = set(map(int, s1.split()))
set2 = set(map(int, s2.split()))
set3 = set(map(int, s3.split()))

# 1) Все уникальные числа по возрастанию (объединение)
all_unique = sorted(set1 | set2 | set3)

# 2) Числа, которые есть в каждой строке (пересечение)
common = sorted(set1 & set2 & set3)

# 3) Числа, которые есть только в одной из трех строк
only_one = sorted((set1 ^ set2 ^ set3) - (set1 & set2 & set3))

# Вывод
print(*all_unique)
print(*common)
print(*only_one)

'''
Программа должна запросить любую фразу и вывести на экран :
     - количество символов в данной фразе.
     - количество слов  в данной фразе. 
            Словом может считаться любой набор символов разделенный от 
            других пробелом и количеством символов больше или равных 1.
     -* количество гласных в данной фразе. Нельзя использовать if и for.

'''

text = input("Введите фразу: ")

# Количество символов
char_count = len(text)

# Количество слов
words = text.split()
word_count = len(words)

# Количество гласных (русские и английские)
vowels = "аеёиоуыэюяaeiouyAEIOUYАЕЁИОУЫЭЮЯ"
vowel_count = sum(map(text.lower().count, vowels))

# phrase.lower() — приводим фразу к нижнему регистру
# map(phrase.count, vowels) — применяем count() ко всем гласным, считая каждую букву
# sum(...) — складываем все

# Вывод результатов
print("Количество символов:", char_count)
print("Количество слов:", word_count)
print("Количество гласных:", vowel_count)

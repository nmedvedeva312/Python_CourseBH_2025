'''
Даны данные пользователя в строке:
    s = "имя: Дмитрий, фамилия: Иванов, возраст: 18"
    Программа должна достать из этой строки, имя, фамилию и возраст пользователя 
    присвоить их отдельным переменным и вывести на экран  через один print(), 
    но каждый элемент должен быть выведен в новой строке. 
    Программа должна работать с любой аналогичной строкой с данными др. человека.
        пример вывода:        
            - Дмитрий
            - Иванов
            - 18

'''

s = "имя: Дмитрий, фамилия: Иванов, возраст: 18"

# Вариант 1

# Разбиваем строку на части по запятой
parts = s.split(",")

# Для каждой части отделяем ключ и значение
name = parts[0].split(": ")[1]
surname = parts[1].split(": ")[1]
age = parts[2].split(": ")[1]

# Или с использованием strip() для удаления лишних пробелов
# name = parts[0].split(": ")[1].strip()
# surname = parts[1].split(": ")[1].strip()
# age = parts[2].split(": ")[1].strip()

# Выводим каждое значение с новой строки
print(f"- {name}\n- {surname}\n- {age}")


# Вариант 2 с использованием словаря

# Разбиваем строку на пары ключ-значение и создаём словарь
data = dict(item.strip().split(": ") for item in s.split(","))

# Извлекаем значения
name = data["имя"]
surname = data["фамилия"]
age = data["возраст"]

# Выводим каждое значение с новой строки
print(f"- {name}\n- {surname}\n- {age}")

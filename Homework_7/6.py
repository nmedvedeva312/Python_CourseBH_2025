"""
1. Запросить у пользователей имя и отзыв о магазине. 
Программа должна запрашивать данные пока не введено слово "stop". 
Все данные сложить в словарь.
    -распечатать количество отзывов
    -распечатать отдельно имена пользователей
    -распечатать отдельно отзывы

"""

reviews = {}

while True:
    name = input("Введите имя пользователя (или 'stop' для выхода): ").strip()
    if name.lower() == "stop":
        break
    review = input(f"Введите отзыв пользователя {name}: ").strip()
    reviews[name] = review

print(f"\nВсего отзывов: {len(reviews)}")

print("Имена пользователей:")
for name in reviews.keys():
    print(name)

print("\nОтзывы пользователей:")
for review in reviews.values():
    print(review)

# Цикл продолжается пока пользователь не введёт "stop" вместо имени.
# Каждый отзыв сохраняется в словаре reviews с ключом — именем пользователя.

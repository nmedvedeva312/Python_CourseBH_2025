"""
Запросить любое число не менее 10. 
Вывести на экран сумму квадратов каждой цифры составляющей это число. 
Например: дано 236 => 2*2 + 3*3 + 6*6 = 49 

"""

while True:
    num_str = input("Введите число не менее 10: ").strip()
    if not num_str.isdigit():
        print("Ошибка: введите число.")
        continue
    if int(num_str) < 10:
        print("Ошибка: число должно быть не менее 10.")
        continue
    break

total = 0
for ch in num_str:
    digit = int(ch)
    total += digit ** 2

print(f"Сумма квадратов цифр числа {num_str} = {total}")

# Запрашиваем число, проверяем что оно целое и >= 10.
# Проходим по каждой цифре, считаем квадрат и суммируем.

"""
Запросить у пользователя год рождения и в соответствии с его возрастом 
охарактеризовать пользователя - 
ребенок, подросток, юноша, в расцвете сил, пожилой, старик.
"""

# Запрашиваем текущий год и год рождения
current_year = int(input("Введите текущий год: "))
birth_year = int(input("Введите ваш год рождения: "))

# Вычисляем возраст
age = current_year - birth_year

# Классификация по возрасту
if age <= 12:
    print("Вы: ребенок")
elif age <= 18:
    print("Вы: подросток")
elif age <= 25:
    print("Вы: юноша")
elif age <= 55:
    print("Вы: в расцвете сил")
elif age <= 85:
    print("Вы: пожилой")
else:
    print("Вы: старик")